// Current language state - make it globally accessible
window.currentLanguage = 'mr';

// Initialize language from localStorage or default to Marathi
function initializeLanguage() {
  const savedLanguage = localStorage.getItem('selectedLanguage');
  if (savedLanguage && translations[savedLanguage]) {
    window.currentLanguage = savedLanguage;
  } else {
    // Default to Marathi
    window.currentLanguage = 'mr';
  }
  return window.currentLanguage;
}

// Get translation for a key
function t(key) {
  const keys = key.split('.');
  let value = translations[window.currentLanguage];
  
  for (const k of keys) {
    if (value && value[k]) {
      value = value[k];
    } else {
      // Fallback to English if translation not found
      value = translations.en;
      for (const fallbackKey of keys) {
        if (value && value[fallbackKey]) {
          value = value[fallbackKey];
        } else {
          return key; // Return key if no translation found
        }
      }
      break;
    }
  }
  
  return value || key;
}

// Switch language
function switchLanguage(lang) {
  if (translations[lang]) {
    window.currentLanguage = lang;
    localStorage.setItem('selectedLanguage', lang);
    updateAllTranslations();
    updateLanguageToggle();
  }
}

// Update language toggle button state
function updateLanguageToggle() {
  const langOptions = document.querySelectorAll('.lang-option');
  langOptions.forEach(option => {
    if (option.getAttribute('data-lang') === window.currentLanguage) {
      option.classList.add('active');
    } else {
      option.classList.remove('active');
    }
  });
}
